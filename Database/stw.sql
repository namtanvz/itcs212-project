
DROP DATABASE IF EXISTS stw;
CREATE DATABASE IF NOT EXISTS `stw` DEFAULT CHARACTER SET utf8 COLLATE utf8_unicode_ci;
USE `stw`;

-- Personal information
CREATE TABLE personal_info(
	uid				INT          PRIMARY KEY,  
	fname			VARCHAR(45)  NOT NULL,
	lname			VARCHAR(45)	 NOT NULL,
    email			VARCHAR(45)	 NOT NULL,
	gender			CHAR(1)	 	 NOT NULL,
    DOB				DATETIME,
    tel				VARCHAR(25)
);
-- login information
CREATE TABLE login(
	lid				INT          PRIMARY KEY,  
	username		VARCHAR(45)  NOT NULL,
	pass			VARCHAR(45)	 NOT NULL,
    l_role			CHAR(1)		 NOT NULL,
    CONSTRAINT FK_ID  FOREIGN KEY (lid) REFERENCES personal_info(uid),
    CONSTRAINT chk_role CHECK (l_role = 'U' OR l_role= 'A')
);
-- Country
CREATE TABLE country (
	coid		CHAR(2) 	 PRIMARY KEY,
    cname	VARCHAR(50)  	 NOT NULL
);

-- Address
CREATE TABLE address(
	aid				INT          PRIMARY KEY,  
	afname			VARCHAR(45)  NOT NULL,
	alname			VARCHAR(45)	 NOT NULL,
    atel			VARCHAR(20)	 NOT NULL,
    city			VARCHAR(45)  NOT NULL,
    province		VARCHAR(45)	 NOT NULL,
    zipcode			VARCHAR(5)	 NOT NULL,
    adescr			VARCHAR(60),
    ccoid			CHAR(2)		 NOT NULL,
    cuid			INT			 NOT NULL,
    CONSTRAINT FK_countryID  FOREIGN KEY (ccoid) REFERENCES country(coid),
    CONSTRAINT FK_userID  	 FOREIGN KEY (cuid) REFERENCES personal_info(uid)
);

-- category
CREATE TABLE category(
	cid			CHAR(2) 	 PRIMARY KEY,
    ctname		VARCHAR(50)  	 NOT NULL
);

-- Product
CREATE TABLE product(
	pid				INT          PRIMARY KEY,  
	cid				INT   		 NOT NULL,
	p_name			VARCHAR(45)	 NOT NULL,
    price			FLOAT	 	 NOT NULL,
    color			VARCHAR(10)  NOT NULL,
    p_desc			VARCHAR(400) NOT NULL
);

